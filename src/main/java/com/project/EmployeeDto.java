package com.project;

public class EmployeeDto {
    public Long id;
    public String firstName;
    public String lastName;
    public String emailId;
    public String role;

    public Long getId() {
        return id;
    }

    public String getFirstName() {
        return firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public String getEmailId() {
        return emailId;
    }

    public String getRole() {
        return role;
    }
}
